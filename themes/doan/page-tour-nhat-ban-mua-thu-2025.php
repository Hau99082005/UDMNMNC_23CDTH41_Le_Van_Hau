<script>
document.addEventListener('DOMContentLoaded', function() {
    // Quantity selector functionality
    const qtyBtns = document.querySelectorAll('.qty-btn');
    qtyBtns.forEach(btn => {
        btn.addEventListener('click', function() {
            const target = this.getAttribute('data-target');
            const input = document.getElementById(target);
            const isPlus = this.classList.contains('plus');
            const currentValue = parseInt(input.value);
            
            if (isPlus) {
                input.value = currentValue + 1;
            } else if (currentValue > (target === 'adults' ? 1 : 0)) {
                input.value = currentValue - 1;
            }
            
            calculateTotal();
        });
    });
    
    // Calculate total price
    function calculateTotal() {
        const adults = parseInt(document.getElementById('adults').value);
        const children = parseInt(document.getElementById('children').value);
        const infants = parseInt(document.getElementById('infants').value);
        const basePrice = 25990000;
        
        const total = (adults * basePrice) + (children * basePrice * 0.7) + (infants * basePrice * 0.3);
        document.getElementById('total-amount').textContent = total.toLocaleString('vi-VN') + ' VNĐ';
    }
    
    // Initial calculation
    calculateTotal();
});
</script>

